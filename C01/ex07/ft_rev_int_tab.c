void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	temp;

	if (tab != 0x0)
	{
		i = 0;
		while (i < size / 2)
		{
			temp = tab[i];
			tab[i] = tab[size - i - 1];
			tab[size - i - 1] = temp;
			i++;
		}
	}
}
